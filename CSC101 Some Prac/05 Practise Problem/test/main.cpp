#include <iostream>

using namespace std;

int main()
{
    int i,k,sum,n;

    cout << "Enter n = ";
    cin >>n;
    sum=0;
    for(i=0;sum<n;i=i+1){
        n%i==k;
        if(k==0){
        sum=sum+i;

        break;
        }
    }
    if(sum==n){
            cout<<"n";
            }


    return 0;
}
